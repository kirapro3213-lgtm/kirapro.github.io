// SCRIPT PARA RELLENAR LA PLANTILLA
document.addEventListener("DOMContentLoaded", () => {
    // Busca el ID en la URL
    const parametrosURL = new URLSearchParams(window.location.search);
    const idNoticia = parametrosURL.get('id');

    // Si estamos en la página de la noticia y hay un ID válido
    if (idNoticia !== null && document.getElementById('titular')) {
        const noticia = baseDeDatos[idNoticia];
        
        if (noticia) {
            document.getElementById('titular').innerText = noticia.titulo;
            document.getElementById('imagen-principal').src = noticia.imagen;
            // Usamos innerHTML para respetar los párrafos <p>
            document.getElementById('cuerpo-texto').innerHTML = noticia.texto;

            // LÓGICA PARA MÚLTIPLES VIDEOS
            const gridContainer = document.getElementById('videos-grid-container');
            if (noticia.videos && noticia.videos.length > 0) {
                // Creamos la cuadrícula de videos
                let videosHTML = '<div class="videos-grid">';
                noticia.videos.forEach(videoUrl => {
                    videosHTML += `<div class="video-item"><iframe src="${videoUrl}" frameborder="0" allowfullscreen></iframe></div>`;
                });
                videosHTML += '</div>';
                gridContainer.innerHTML = videosHTML;
            } else {
                // Ocultamos si no hay videos
                gridContainer.style.display = 'none';
            }
        } else {
            // ... (lógica de noticia no encontrada igual que antes) ...
        }
    }
});
